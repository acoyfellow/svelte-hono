import { buildHonoSvelte } from "../../dist/build.js";
import { createHighlighter } from "shiki";
import { readFileSync, writeFileSync } from "node:fs";
import { snippets } from "./snippets.mjs";

// ── 0. Pin the svelte-hono version once, so docs never drift. ────────────
const pkg = JSON.parse(readFileSync(new URL("../../package.json", import.meta.url), "utf8"));
writeFileSync(
  new URL("./version.generated.js", import.meta.url),
  `// Generated by build.mjs from ../../package.json. Do not edit.\nexport const version = ${JSON.stringify(pkg.version)};\n`,
);
console.log(`✓ version    v${pkg.version}`);

// ── 1. Pre-render snippets with shiki at build time. ──────────────────────
const highlighter = await createHighlighter({
  themes: ["github-light"],
  langs: ["ts", "javascript", "svelte", "html", "bash", "toml", "json"],
});

const rendered = {};
for (const [key, snip] of Object.entries(snippets)) {
  const lang = highlighter.getLoadedLanguages().includes(snip.lang) ? snip.lang : "text";
  rendered[key] = highlighter.codeToHtml(snip.code, {
    lang,
    theme: "github-light",
  });
}

writeFileSync(
  new URL("./snippets.generated.js", import.meta.url),
  `// Generated by build.mjs from snippets.mjs. Do not edit.
export const snippets = ${JSON.stringify(rendered)};
`,
);
console.log(`✓ snippets  ${Object.keys(rendered).length} pre-highlighted with shiki`);
highlighter.dispose();

// ── 2. Build the worker. ──────────────────────────────────────────────────
const result = await buildHonoSvelte({
  workerEntry: "./worker.ts",
  outDir: "./build",
  components: {
    home: "./Home.svelte",
    start: "./Start.svelte",
    docs: "./Docs.svelte",
    why: "./Why.svelte",
    examples: "./Examples.svelte",
  },
});

console.log(`✓ worker    ${(result.workerBytes / 1024).toFixed(1)} KB`);
for (const [id, sizes] of Object.entries(result.bundleSizes)) {
  console.log(`  client ${id.padEnd(10)}  ${(sizes.js / 1024).toFixed(1)} KB JS + ${(sizes.css / 1024).toFixed(2)} KB CSS`);
}
